/*!
    \fn vfnCfgRainSensorPorts
    \param
    \return
    \brief  Configura los puertos que se van a utilizar en el sensor de lluvia
*/
void vfnCfgRainSensorPorts(void) {
	
	vfnPortPCR(1,1,0);
}
/*!
    \fn vfnIsRaining
    \param
    \return regresa un 0 o un 1
    \brief  Sirve para checar el estado del sensor de lluvia
*/
U08 vfnIsRaining(void) {
	
	if(!u08CheckBit(PORTB0)) {
		return 1;
	}
	return 0;
}

